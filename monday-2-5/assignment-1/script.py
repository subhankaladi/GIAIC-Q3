# Data Types Example

# Integer
integer_value = 10
print(integer_value)

# String
string_value = "Hello, World!"
print(string_value)

# Float
float_value = 3.14
print(float_value)

# Boolean
bool_value = True
print(bool_value)

# List
list_value: str = ["subhan" , "kaladi"]
print(list_value)

# Dictionary
dictionary_value = {"name": "Subhan Kaladi", "age": 20}
print(dictionary_value)

