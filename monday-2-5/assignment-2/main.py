# Arithmetic Operators
a = 10
b = 5
print("Addition:", a + b)
print("Subtraction:", a - b)
print("Multiplication:", a * b)
print("Division:", a / b)
print("Modulus:", a % b)
print("Exponent:", a ** b)
print("Floor Division:", a // b)

# Assignment Operators
x = 5
x += 3
print("x += 3:", x)
x -= 2
print("x -= 2:", x)
x *= 4
print("x *= 4:", x)
x /= 2
print("x /= 2:", x)

# Comparison Operators
print("Is 10 > 5?", 10 > 5)
print("Is 10 == 10?", 10 == 10)
print("Is 5 != 2?", 5 != 2)

# Logical Operators
a = True
b = False
print("a and b:", a and b)
print("a or b:", a or b)
print("not a:", not a)

# Identity Operators
x = ["apple", "banana"]
y = x
z = ["apple", "banana"]
print("x is y:", x is y)
print("x is z:", x is z)
print("x == z:", x == z)

# Membership Operators
fruits = ["apple", "banana", "cherry"]
print("'banana' in fruits:", "banana" in fruits)
print("'mango' not in fruits:", "mango" not in fruits)

# Bitwise Operators
a = 5  # 0101
b = 3  # 0011
print("a & b:", a & b)
print("a | b:", a | b)
print("a ^ b:", a ^ b)
print("~a:", ~a)
print("a << 1:", a << 1)
print("a >> 1:", a >> 1)
